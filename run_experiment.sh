#!/usr/bin/env bash
# run_experiment.sh  –  Run experiments and collect results.
#
# Runs Go services locally with "go run" (no Docker required) for the failover
# benchmark, and pure-Python simulations for the QoS trade-off and controlled
# degradation evaluations.
#
# Usage:
#   ./run_experiment.sh                                    # all, basic scenario, 30 runs
#   ./run_experiment.sh --runs 50 --seed 1234              # reproducible run
#   ./run_experiment.sh --eval-scenario basic              # explicit basic scenario
#   ./run_experiment.sh --eval-scenario improved01         # strengthened scenario
#   ./run_experiment.sh --scenario basic                   # alias: same as --eval-scenario basic
#   ./run_experiment.sh --scenario improved01              # alias: same as --eval-scenario improved01
#   ./run_experiment.sh --scenario tradeoff                # only QoS trade-off (no Go services)
#   ./run_experiment.sh --scenario degradation             # only degradation  (no Go services)
#   ./run_experiment.sh --scenario failover                # only Go benchmark
#   ./run_experiment.sh --no-start                         # skip Go service startup
#   ./run_experiment.sh --output-dir /tmp/exp              # custom output directory
#   ./run_experiment.sh --help
#
# Output is namespaced by eval-scenario:
#   <output-dir>/<eval-scenario>/manifest.json
#   <output-dir>/<eval-scenario>/tradeoff/aggregated.csv
#   <output-dir>/<eval-scenario>/degradation/aggregated.csv
#   docs/figures/<eval-scenario>/*.{png,pdf}
#
# Seed schedule:
#   seed_i = BASE_SEED + i   (i = 0 .. runs-1)
#   If --seed is not given, a random base seed is generated once, printed,
#   and logged in <output-dir>/manifest.json for reproducibility.
#
# Output:
#   <output-dir>/manifest.json                       ← seed and parameter log
#   <output-dir>/tradeoff/aggregated.csv             ← QoS trade-off data
#   <output-dir>/tradeoff/runs/run_NNNN_seed_S.csv   ← per-run details
#   <output-dir>/degradation/aggregated.csv          ← degradation data
#   <output-dir>/degradation/runs/run_NNNN_seed_S.csv
#   <output-dir>/failover_delay_vs_network_delay.csv ← Go benchmark (failover)
#   <output-dir>/failover_events.csv
#   docs/figures/*.{png,pdf}                         ← publication-ready plots

set -euo pipefail

# ── Defaults ─────────────────────────────────────────────────────────────────
RUNS=30
BASE_SEED=""            # empty = auto-generated by Python
DO_START=true
OUTPUT_DIR="$(pwd)/results"
SCENARIO="all"          # tradeoff | degradation | failover | all
EVAL_SCENARIO="basic"   # basic | improved01
BASELINE="both"         # qos_aware | availability_based | both
SCENARIO_URL="http://localhost:8700"
TIMEOUT_STARTUP=60
TIMEOUT_EXPERIMENT=600

# ── Argument parsing ──────────────────────────────────────────────────────────
while [[ $# -gt 0 ]]; do
  case $1 in
    --runs)          RUNS="$2";          shift 2 ;;
    --seed)          BASE_SEED="$2";     shift 2 ;;
    --output-dir)    OUTPUT_DIR="$2";    shift 2 ;;
    --eval-scenario) EVAL_SCENARIO="$2"; shift 2 ;;
    --baseline)      BASELINE="$2";      shift 2 ;;
    --no-start)      DO_START=false;     shift   ;;
    --scenario)
      # Accept both old values (tradeoff/degradation/failover/all) and
      # new eval-scenario aliases (basic/improved01).  The two name spaces
      # do not overlap, so we can dispatch cleanly.
      case "$2" in
        basic|improved01)
          EVAL_SCENARIO="$2"
          # Keep SCENARIO as "all" so both experiment types still run.
          ;;
        tradeoff|degradation|failover|all)
          SCENARIO="$2"
          ;;
        *)
          echo "Unknown --scenario value: $2"; exit 1 ;;
      esac
      shift 2
      ;;
    --help|-h)
      grep '^#' "$0" | sed 's/^# \{0,2\}//'
      exit 0
      ;;
    *) echo "Unknown option: $1"; exit 1 ;;
  esac
done

# ── Colours ───────────────────────────────────────────────────────────────────
GREEN='\033[0;32m'; YELLOW='\033[1;33m'; RED='\033[0;31m'; BLUE='\033[0;34m'; NC='\033[0m'
info() { echo -e "${BLUE}[INFO]${NC}  $*"; }
ok()   { echo -e "${GREEN}[ OK ]${NC}  $*"; }
warn() { echo -e "${YELLOW}[WARN]${NC}  $*"; }
die()  { echo -e "${RED}[ERR ]${NC}  $*" >&2; exit 1; }

# ── Announce seed ─────────────────────────────────────────────────────────────
echo ""
info "Experiment configuration"
info "  Scenario       : $SCENARIO"
info "  Eval scenario  : $EVAL_SCENARIO"
info "  Runs           : $RUNS"
if [[ -n "$BASE_SEED" ]]; then
  info "  Base seed      : $BASE_SEED  (user-provided)"
else
  info "  Base seed      : (auto-generated – will be printed by Python and saved to manifest.json)"
fi
info "  Output dir     : $OUTPUT_DIR/$EVAL_SCENARIO"
info "  Figures dir    : docs/figures/$EVAL_SCENARIO"
echo ""

mkdir -p "$OUTPUT_DIR"

# ── Prerequisites ─────────────────────────────────────────────────────────────
command -v python3 >/dev/null 2>&1 || die "python3 not found"
command -v curl    >/dev/null 2>&1 || die "curl not found"
command -v jq      >/dev/null 2>&1 && HAS_JQ=true || HAS_JQ=false

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BACKEND="$SCRIPT_DIR/backend"
FIGURES_DIR="$SCRIPT_DIR/docs/figures"

# Resolve Python: prefer the project venv if it exists
if [[ -x "$SCRIPT_DIR/.venv/bin/python" ]]; then
  PYTHON="$SCRIPT_DIR/.venv/bin/python"
  info "Using venv Python: $PYTHON"
else
  PYTHON="python3"
  # Check for required packages
  if ! "$PYTHON" -c "import numpy, matplotlib" 2>/dev/null; then
    warn "numpy / matplotlib not found.  Installing into a local venv…"
    python3 -m venv "$SCRIPT_DIR/.venv"
    "$SCRIPT_DIR/.venv/bin/pip" install --quiet numpy matplotlib
    PYTHON="$SCRIPT_DIR/.venv/bin/python"
    ok "Dependencies installed."
  fi
fi

# ── ─────────────────────────────────────────────────────────────────────────
# PART A: Python simulations (tradeoff + degradation)
# These do NOT require Go services.
# ── ─────────────────────────────────────────────────────────────────────────

NEED_PYTHON=false
[[ "$SCENARIO" == "tradeoff"    ]] && NEED_PYTHON=true
[[ "$SCENARIO" == "degradation" ]] && NEED_PYTHON=true
[[ "$SCENARIO" == "all"         ]] && NEED_PYTHON=true

if [[ "$NEED_PYTHON" == true ]]; then
  PYTHON_SCENARIO="$SCENARIO"
  # failover is Go-only; tell Python to run only its own scenarios
  [[ "$SCENARIO" == "all" ]] && PYTHON_SCENARIO="all"
  [[ "$SCENARIO" == "failover" ]] && NEED_PYTHON=false
fi

if [[ "$NEED_PYTHON" == true ]]; then
  echo ""
  info "Running Python simulations (scenario=$PYTHON_SCENARIO, runs=$RUNS)…"

  SEED_FLAG=""
  [[ -n "$BASE_SEED" ]] && SEED_FLAG="--seed $BASE_SEED"

  "$PYTHON" "$SCRIPT_DIR/scripts/experiments.py" \
    --runs "$RUNS" \
    $SEED_FLAG \
    --output-dir "$OUTPUT_DIR" \
    --scenario "$PYTHON_SCENARIO" \
    --eval-scenario "$EVAL_SCENARIO" \
    --baseline "$BASELINE"

  ok "Python simulations complete."
fi

# ── ─────────────────────────────────────────────────────────────────────────
# PART B: Go failover benchmark (requires running services)
# ── ─────────────────────────────────────────────────────────────────────────

NEED_GO=false
[[ "$SCENARIO" == "failover" ]] && NEED_GO=true
[[ "$SCENARIO" == "all"      ]] && NEED_GO=true

if [[ "$NEED_GO" == true ]]; then

  PIDS=()
  cleanup() {
    if [[ ${#PIDS[@]} -gt 0 ]]; then
      info "Stopping background services…"
      for pid in "${PIDS[@]}"; do kill "$pid" 2>/dev/null || true; done
      wait 2>/dev/null || true
      ok "Services stopped."
    fi
  }
  [[ "$DO_START" == true ]] && trap cleanup EXIT INT TERM

  wait_for() {
    local label="$1" url="$2"
    local elapsed=0 interval=2
    printf "  Waiting for %-22s " "$label…"
    while ! curl -sf --max-time 2 "$url" >/dev/null 2>&1; do
      sleep $interval; elapsed=$((elapsed + interval)); printf "."
      [[ $elapsed -ge $TIMEOUT_STARTUP ]] && { echo ""; die "$label did not respond within ${TIMEOUT_STARTUP}s"; }
    done
    echo -e "  ${GREEN}up${NC}"
  }

  if [[ "$DO_START" == true ]]; then
    command -v go >/dev/null 2>&1 || die "go not found – install Go 1.21+"
    echo ""
    info "Building and starting all Go services with 'go run'…"
    info "(First run compiles everything – may take ~30s)"
    echo ""

    cd "$BACKEND"

    PORT=8000 go run ./cmd/arrowhead &
    PIDS+=($!); info "arrowhead       :8000"
    sleep 3

    PORT=8101 IDT_ID=idt1a IDT_NAME="Inspection Robot A" ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-robot &
    PIDS+=($!); info "idt1a           :8101"

    PORT=8102 IDT_ID=idt1b IDT_NAME="Inspection Robot B" ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-robot &
    PIDS+=($!); info "idt1b           :8102"

    PORT=8201 IDT_ID=idt2a IDT_NAME="Gas Sensing Unit A" ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-gas &
    PIDS+=($!); info "idt2a           :8201"

    PORT=8202 IDT_ID=idt2b IDT_NAME="Gas Sensing Unit B" ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-gas &
    PIDS+=($!); info "idt2b           :8202"

    PORT=8301 IDT_ID=idt3a IDT_NAME="LHD Vehicle A"      ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-lhd &
    PIDS+=($!); info "idt3a           :8301"

    PORT=8302 IDT_ID=idt3b IDT_NAME="LHD Vehicle B"      ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-lhd &
    PIDS+=($!); info "idt3b           :8302"

    PORT=8401 IDT_ID=idt4  IDT_NAME="Tele-Remote"        ARROWHEAD_URL=http://localhost:8000 go run ./cmd/idt-teleremote &
    PIDS+=($!); info "idt4            :8401"

    sleep 3

    PORT=8501 ARROWHEAD_URL=http://localhost:8000 \
      IDT1A_URL=http://localhost:8101 IDT1B_URL=http://localhost:8102 \
      LOG_DIR="$OUTPUT_DIR" go run ./cmd/cdt1 &
    PIDS+=($!); info "cdt1            :8501"

    PORT=8502 ARROWHEAD_URL=http://localhost:8000 \
      IDT2A_URL=http://localhost:8201 IDT2B_URL=http://localhost:8202 \
      LOG_DIR="$OUTPUT_DIR" go run ./cmd/cdt2 &
    PIDS+=($!); info "cdt2            :8502"

    PORT=8503 ARROWHEAD_URL=http://localhost:8000 \
      CDT1_URL=http://localhost:8501 CDT2_URL=http://localhost:8502 \
      IDT1A_URL=http://localhost:8101 IDT1B_URL=http://localhost:8102 \
      go run ./cmd/cdt3 &
    PIDS+=($!); info "cdt3            :8503"

    PORT=8504 ARROWHEAD_URL=http://localhost:8000 \
      IDT3A_URL=http://localhost:8301 IDT3B_URL=http://localhost:8302 \
      go run ./cmd/cdt4 &
    PIDS+=($!); info "cdt4            :8504"

    PORT=8505 ARROWHEAD_URL=http://localhost:8000 \
      IDT4_URL=http://localhost:8401 \
      go run ./cmd/cdt5 &
    PIDS+=($!); info "cdt5            :8505"

    sleep 3

    PORT=8601 ARROWHEAD_URL=http://localhost:8000 \
      CDT1_URL=http://localhost:8501 CDT3_URL=http://localhost:8503 \
      CDT4_URL=http://localhost:8504 CDT5_URL=http://localhost:8505 \
      LOG_DIR="$OUTPUT_DIR" go run ./cmd/cdta &
    PIDS+=($!); info "cdta            :8601"

    PORT=8602 ARROWHEAD_URL=http://localhost:8000 \
      CDT2_URL=http://localhost:8502 CDT3_URL=http://localhost:8503 \
      LOG_DIR="$OUTPUT_DIR" go run ./cmd/cdtb &
    PIDS+=($!); info "cdtb            :8602"

    sleep 2

    PORT=8700 ARROWHEAD_URL=http://localhost:8000 \
      IDT1A_URL=http://localhost:8101 IDT1B_URL=http://localhost:8102 \
      IDT2A_URL=http://localhost:8201 IDT2B_URL=http://localhost:8202 \
      IDT3A_URL=http://localhost:8301 IDT3B_URL=http://localhost:8302 \
      IDT4_URL=http://localhost:8401 \
      CDT1_URL=http://localhost:8501 CDT2_URL=http://localhost:8502 \
      CDT3_URL=http://localhost:8503 CDT4_URL=http://localhost:8504 \
      CDT5_URL=http://localhost:8505 \
      CDTA_URL=http://localhost:8601 CDTB_URL=http://localhost:8602 \
      LOG_DIR="$OUTPUT_DIR" go run ./cmd/scenario &
    PIDS+=($!); info "scenario        :8700"

    # Frontend (Vite dev server)
    FRONTEND="$SCRIPT_DIR/frontend"
    if [[ -d "$FRONTEND/node_modules" ]]; then
      cd "$FRONTEND"
      npm run dev -- --port 3000 --host &
      PIDS+=($!); info "frontend        :3000  (http://localhost:3000)"
      cd "$BACKEND"
    else
      warn "frontend/node_modules not found – run 'npm install' in $FRONTEND first"
    fi
  fi  # DO_START

  echo ""
  info "Waiting for services to be healthy…"
  wait_for "arrowhead"       "http://localhost:8000/registry"
  wait_for "idt2a"           "http://localhost:8201/health"
  wait_for "idt2b"           "http://localhost:8202/health"
  wait_for "cdt1"            "http://localhost:8501/health"
  wait_for "cdt2"            "http://localhost:8502/health"
  wait_for "scenario runner" "http://localhost:8700/health"

  info "Waiting 5s for Arrowhead registration to settle…"
  sleep 5

  # Use RUNS as runsPerPoint for the failover benchmark
  RUNS_PER_POINT="$RUNS"
  TOTAL_RUNS=$(( 11 * 2 * RUNS_PER_POINT ))
  echo ""
  info "Launching failover benchmark"
  info "  runsPerPoint = $RUNS_PER_POINT"
  info "  total runs   = $TOTAL_RUNS  (11 delays × 2 modes × $RUNS_PER_POINT)"

  RESP=$(curl -sf -X POST "$SCENARIO_URL/scenario/experiment/run" \
    -H "Content-Type: application/json" \
    -d "{\"runsPerPoint\": $RUNS_PER_POINT}")
  echo "$RESP"

  echo ""
  info "Progress (polling every 5s)…"
  elapsed=0
  while true; do
    STATUS=$(curl -sf "$SCENARIO_URL/scenario/experiment/results" 2>/dev/null \
             || echo '{"status":"unreachable"}')
    STATE=$(echo "$STATUS" | \
            python3 -c "import sys,json; print(json.load(sys.stdin).get('status','?'))" 2>/dev/null \
            || echo "$STATUS" | grep -oP '"status"\s*:\s*"\K[^"]+' || echo "?")

    case "$STATE" in
      completed)  echo ""; ok "Failover benchmark complete!"; break ;;
      running)    printf "." ;;
      unreachable) printf "?" ;;
      *)          printf "[$STATE]" ;;
    esac
    sleep 5; elapsed=$((elapsed + 5))
    [[ $elapsed -ge $TIMEOUT_EXPERIMENT ]] && die "Timed out after ${TIMEOUT_EXPERIMENT}s"
  done

  # Print results table
  echo ""
  RESULTS=$(curl -sf "$SCENARIO_URL/scenario/experiment/results")
  echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
  echo -e "${BLUE}  RESULTS  –  Decision delay (detection → switch)${NC}"
  echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
  if $HAS_JQ; then
    echo "$RESULTS" | jq -r '
      ["delay_ms","local_ms","central_ms"],
      (.results.summary[] | [.networkDelayMs,
        (.avgLocalDecisionMs|.*100|round/100),
        (.avgCentralDecisionMs|.*100|round/100)])
      | @tsv' | column -t
  else
    echo "$RESULTS" | python3 -c "
import sys, json
d = json.load(sys.stdin)
print(f\"{'delay_ms':>10}  {'local_ms':>10}  {'central_ms':>12}\")
for s in d.get('results', {}).get('summary', []):
    print(f\"{s['networkDelayMs']:>10}  {s['avgLocalDecisionMs']:>10.1f}  {s['avgCentralDecisionMs']:>12.1f}\")
" 2>/dev/null || echo "$RESULTS"
  fi

  # Copy failover CSV to output dir
  FAILOVER_CSV="$OUTPUT_DIR/failover_delay_vs_network_delay.csv"
  if [[ -f "$FAILOVER_CSV" ]]; then
    ok "Failover CSV: $FAILOVER_CSV"
  fi

fi  # NEED_GO

# ── ─────────────────────────────────────────────────────────────────────────
# PART C: Generate plots
# ── ─────────────────────────────────────────────────────────────────────────

echo ""
info "Generating publication-quality plots…"

PLOT_SCENARIO="$SCENARIO"
# Map "all" to include failover plot only if we actually ran it
if [[ "$SCENARIO" == "all" && "$NEED_GO" == false ]]; then
  PLOT_SCENARIO="all"   # plot.py skips failover CSV gracefully if not present
fi

"$PYTHON" "$SCRIPT_DIR/scripts/plot.py" \
  --input-dir     "$OUTPUT_DIR" \
  --output-dir    "$FIGURES_DIR" \
  --scenario      "$PLOT_SCENARIO" \
  --eval-scenario "$EVAL_SCENARIO"

ok "Plots written to: $FIGURES_DIR"

# ── Summary ───────────────────────────────────────────────────────────────────
echo ""
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${BLUE}  Output summary${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
EVAL_OUT="$OUTPUT_DIR/$EVAL_SCENARIO"
EVAL_FIG="$FIGURES_DIR/$EVAL_SCENARIO"
for f in \
  "$EVAL_OUT/manifest.json" \
  "$EVAL_OUT/tradeoff/aggregated.csv" \
  "$EVAL_OUT/degradation/aggregated.csv" \
  "$OUTPUT_DIR/failover_delay_vs_network_delay.csv" \
  "$EVAL_FIG/tradeoff_provider_utilities.png" \
  "$EVAL_FIG/tradeoff_qos_metrics_vs_weight.png" \
  "$EVAL_FIG/degradation_combined.png" \
  "$EVAL_FIG/failover_delay_vs_network_delay.png"; do
  [[ -f "$f" ]] && ok "  $f" || warn "  $f  (not generated)"
done

MANIFEST="$EVAL_OUT/manifest.json"
if [[ -f "$MANIFEST" ]]; then
  echo ""
  SEED_VAL=$(python3 -c "import json; print(json.load(open('$MANIFEST'))['base_seed'])" 2>/dev/null || echo "?")
  info "Base seed used: $SEED_VAL"
  info "Reproduce with:"
  info "  ./run_experiment.sh --seed $SEED_VAL --runs $RUNS --scenario $SCENARIO --eval-scenario $EVAL_SCENARIO"
fi

if [[ "$NEED_GO" == true && "$DO_START" == true ]]; then
  echo ""
  info "Go services still running. Press Ctrl+C to stop them."
  info "Frontend: http://localhost:3000"
  wait
fi
